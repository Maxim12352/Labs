def видалити_повторення(список):
    # Створюємо новий список для збереження унікальних елементів
    унікальний_список = []
    
    # Ітеруємося по введеному списку
    for елемент in список:
        # Якщо елемент ще не зустрічався в унікальному списку, то додаємо його туди
        if елемент not in унікальний_список:
            унікальний_список.append(елемент)
    
    return унікальний_список

# Зчитуємо список з клавіатури
введений_список = input("Введіть список елементів через пробіл: ").split()

# Конвертуємо введений список до типу int (або float, залежно від типу елементів)
введений_список = [int(елемент) for елемент in введений_список]

# Викликаємо функцію для видалення повторень
унікальний_список = видалити_повторення(введений_список)

# Виводимо унікальний список на екран
print("Унікальний список без повторень:", унікальний_список)

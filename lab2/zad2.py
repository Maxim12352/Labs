# Зчитуємо значення щомісячної стипендії (A) та початкових витрат на проживання (B) з клавіатури
A = float(input("Введіть щомісячну стипендію (грн): "))
B = float(input("Введіть витрати на проживання на початку навчального року (грн): "))

# Ініціалізуємо змінну для суми грошей, яку потрібно попросити в батьків
total_money_needed = 0

# Розраховуємо суму грошей, яку потрібно попросити в батьків на кожний місяць
for month in range(1, 11):  # 10 місяців навчального року
    total_money_needed += B  # Додаємо поточні витрати на проживання до загальної суми
    B *= 1.05  # Збільшуємо витрати на 5% для наступного місяця

# Додаємо останню стипендію на кінець навчального року
total_money_needed += A

# Виводимо суму грошей, яку потрібно попросити в батьків
print(f"Сума грошей, яку потрібно попросити в батьків на навчальний рік: {total_money_needed} грн")

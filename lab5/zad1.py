# Зчитуємо довжину масиву з клавіатури
N = int(input("Введіть довжину масиву: "))

# Ініціалізуємо пустий список для збереження елементів масиву
масив = []

# Зчитуємо елементи масиву з клавіатури та додаємо їх до списку
for _ in range(N):
    елемент = float(input("Введіть елемент масиву: "))
    масив.append(елемент)

# Виводимо всі елементи масиву у зворотному порядку
масив.reverse()
print("Елементи масиву у зворотному порядку:", масив)

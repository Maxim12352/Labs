# Ініціалізуємо двовимірний масив розміром 7x7 і заповнюємо його нулями
рядки = 7
стовпці = 7
масив = [[0 for _ in range(стовпці)] for _ in range(рядки)]

# Заповнюємо масив згідно з вказаним зразком
for i in range(рядки):
    for j in range(стовпці - i):
        масив[i][j] = стовпці - i

# Виводимо масив на екран
for рядок in масив:
    for елемент in рядок:
        print(елемент, end=' ')
    print()

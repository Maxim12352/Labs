# Зчитуємо речення з введення користувача
речення = input("Введіть речення: ")

# Розділяємо речення на слова
слова = речення.split()

# Ініціалізуємо лічильник слів, які закінчуються на "р"
кількість_слів = 0

# Перевіряємо кожне слово в реченні
for слово in слова:
    # Перевіряємо, чи слово закінчується на "р"
    if слово.endswith('р'):
        кількість_слів += 1

# Виводимо результат
print("Кількість слів, які закінчуються на 'р':", кількість_слів)
